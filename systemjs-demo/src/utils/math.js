export function add(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}

export function divide(a, b) {
  return a / b;
}

export function square(a) {
  return multiply(a, b);
}

export function power(a, b) {
  return Math.pow(a, b);
}
